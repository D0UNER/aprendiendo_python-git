# =========================================================
# Operadores aritméticos
# Se usan para realizar operaciones matemáticas.
# =========================================================


# ---------------------------------------------------------
# Suma
# ---------------------------------------------------------
# operador: +
# Ejemplo:
a = 5 + 3   # 8


# ---------------------------------------------------------
# Resta
# ---------------------------------------------------------
# operador: -
# Ejemplo:
a = 10 - 4   # 6


# ---------------------------------------------------------
# Multiplicación
# ---------------------------------------------------------
# operador: *
# Ejemplo:
a = 6 * 3   # 18


# ---------------------------------------------------------
# División
# ---------------------------------------------------------
# operador: /
# Siempre devuelve un float (número decimal) aunque sea un numero entero
a = 10 / 4   # 2.5


# ---------------------------------------------------------
# División entera
# ---------------------------------------------------------
# operador: //
# Devuelve el un entero (int) rendondeado hacia abajo
a = 10 // 4   # 2


# ---------------------------------------------------------
# Módulo (resto)
# ---------------------------------------------------------
# operador: %
# Devuelve el resto de una división
a = 10 % 4   # 2  (porque 10 dividido 4 deja resto 2)


# ---------------------------------------------------------
# Potenciación
# ---------------------------------------------------------
# operador: **
# Eleva un número a una potencia
a = 2 ** 3   # 8  (2 elevado a 3)